    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:
        int fila = jTable1.getSelectedRow();
    if (fila < 0) {
        javax.swing.JOptionPane.showMessageDialog(this, "Por favor, selecciona un alumno de la tabla para eliminar.", "Aviso", javax.swing.JOptionPane.WARNING_MESSAGE);
        return;
    }

    int respuesta = javax.swing.JOptionPane.showConfirmDialog(this, "¿Estás seguro de que deseas eliminar este alumno?", "Confirmar Eliminación", javax.swing.JOptionPane.YES_NO_OPTION, javax.swing.JOptionPane.QUESTION_MESSAGE);
    
    if (respuesta == javax.swing.JOptionPane.YES_OPTION) {
        // Agarramos la matrícula de la fila seleccionada (Columna 1)
        String matricula = jTable1.getValueAt(fila, 1).toString();

        String sql = "DELETE FROM alumnos WHERE matricula = ?";
        try {
            java.sql.Connection con = ConexionBD.getConexion();
            java.sql.PreparedStatement ps = con.prepareStatement(sql);
            ps.setString(1, matricula);

            int resultado = ps.executeUpdate();
            if (resultado > 0) {
                javax.swing.JOptionPane.showMessageDialog(this, "Alumno eliminado correctamente de la base de datos.", "Éxito", javax.swing.JOptionPane.INFORMATION_MESSAGE);
                
                txtMatricula.setEditable(true); // Por si estaba bloqueada
                btnLimpiarActionPerformed(null); // Limpia las cajas de texto
                listarAlumnos(); // Actualiza la lista
            }
        } catch (java.sql.SQLException e) {
            // Esta alerta te protege si intentas borrar un alumno que ya tiene relación con otra tabla
            if (e.getErrorCode() == 1451) {
                javax.swing.JOptionPane.showMessageDialog(this, "No se puede eliminar al alumno porque tiene registros asociados (como calificaciones o materias) en el sistema.", "Error de Llave Foránea", javax.swing.JOptionPane.ERROR_MESSAGE);
            } else {
                javax.swing.JOptionPane.showMessageDialog(this, "Error al eliminar: " + e.getMessage(), "Error", javax.swing.JOptionPane.ERROR_MESSAGE);
            }
        }
    }
    }//GEN-LAST:event_btnEliminarActionPerformed

