    public void listarAlumnos() {
    // Títulos exactos de tu diseño
    javax.swing.table.DefaultTableModel modelo = (javax.swing.table.DefaultTableModel) jTable1.getModel();
    
    // 2. Limpia los datos anteriores para que no se dupliquen
    modelo.setRowCount(0);

    String sql = "SELECT id_alumno, matricula, nombre, apellido_paterno, apellido_materno, semestre, id_carrera FROM alumnos";
    
    try {
        java.sql.Connection con = Conexion.ConexionBD.getConexion();
        java.sql.PreparedStatement ps = con.prepareStatement(sql);
        java.sql.ResultSet rs = ps.executeQuery();
        
        while (rs.next()) {
            // Aquí llenas las 7 columnas usando el modelo que ya existe
            Object[] registro = new Object[7]; 
            registro[0] = rs.getString("id_alumno");
            registro[1] = rs.getString("matricula");
            registro[2] = rs.getString("nombre");
            registro[3] = rs.getString("apellido_paterno");
            registro[4] = rs.getString("apellido_materno");
            registro[5] = rs.getString("semestre");
            registro[6] = rs.getString("id_carrera");
            
            modelo.addRow(registro);
        }
    } catch (Exception e) {
        javax.swing.JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
    }
}
    
    public void filtrarAlumnos(String valor) {
    String[] titulos = {"ID", "Matrícula", "Nombre", "Apellido_PAterno.", "Apellido_Materno.", "Semestre", "id_carrera"};
    javax.swing.table.DefaultTableModel modelo = new javax.swing.table.DefaultTableModel(null, titulos);
    
    // El LIKE '%...%' hace la magia de buscar coincidencias
    String sql = "SELECT * FROM alumnos WHERE nombre LIKE '%" + valor + "%' OR matricula LIKE '%" + valor + "%'";
    
    try {
        java.sql.Connection con = ConexionBD.getConexion();
        java.sql.PreparedStatement ps = con.prepareStatement(sql);
        java.sql.ResultSet rs = ps.executeQuery();
        
        String[] registro = new String[7];
        while (rs.next()) {
            registro[0] = rs.getString("id_alumno");
            registro[1] = rs.getString("matricula");
            registro[2] = rs.getString("nombre");
            registro[3] = rs.getString("apellido_paterno");
            registro[4] = rs.getString("apellido_materno");
            registro[5] = rs.getString("semestre");
            registro[6] = rs.getString("id_carrera");
            modelo.addRow(registro);
        }
        jTable1.setModel(modelo);
    } catch (java.sql.SQLException e) {
        System.out.println("Error al buscar: " + e.getMessage());
    }
}

