    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:
        if (txtMatricula.getText().trim().isEmpty() || txtNombre.getText().trim().isEmpty() || txtApellidoP.getText().trim().isEmpty()) {
        javax.swing.JOptionPane.showMessageDialog(this, "Matrícula, Nombre y Apellido Paterno son obligatorios.", "Advertencia", javax.swing.JOptionPane.WARNING_MESSAGE);
        return;
    }
        String sql = "INSERT INTO alumnos (matricula, nombre, apellido_paterno, apellido_materno, semestre, id_carrera) VALUES (?, ?, ?, ?, ?, ?)";

try {
    java.sql.Connection con = ConexionBD.getConexion();
    java.sql.PreparedStatement ps = con.prepareStatement(sql);

    ps.setString(1, txtMatricula.getText().trim());
    ps.setString(2, txtNombre.getText().trim());
    ps.setString(3, txtApellidoP.getText().trim());
    ps.setString(4, txtApellidoM.getText().trim());
    ps.setString(5, txtSemestre.getText().trim());
    ps.setString(6, txtIdCarrera.getText().trim()); // <-- Aquí capturas el ID de carrera

    int resultado = ps.executeUpdate();
    if (resultado > 0) {
        javax.swing.JOptionPane.showMessageDialog(this, "Alumno registrado con éxito");
        listarAlumnos(); // Refresca la tabla
    }
} catch (java.sql.SQLException e) {
    javax.swing.JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
    }//GEN-LAST:event_btnGuardarActionPerformed
}
    
