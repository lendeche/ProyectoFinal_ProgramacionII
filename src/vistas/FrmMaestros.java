    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:
        try {
        String correo = txtCorreo.getText().trim();
        Connection con = Conexion.ConexionBD.getConexion();
        String sql = "DELETE FROM maestros WHERE correo = ?";
        
        PreparedStatement ps = con.prepareStatement(sql);
        ps.setString(1, correo);
        
        int filasAfectadas = ps.executeUpdate();
        if(filasAfectadas > 0) {
            JOptionPane.showMessageDialog(this, "Maestro eliminado exitosamente");
            btnLimpiarActionPerformed(evt); // Limpiamos campos
        } else {
            JOptionPane.showMessageDialog(this, "No se encontró el maestro con ese correo");
        }
        con.close();
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Error al eliminar: " + e.getMessage());
    }
    }//GEN-LAST:event_btnEliminarActionPerformed

