    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:
        try {
        // 1. Obtener datos de los campos de texto
        String nombre = txtNombre.getText().trim();
        String apeP = txtApellidoPaterno.getText().trim();
        String apeM = txtApellidoMaterno.getText().trim();
        String esp = txtEspecialidad.getText().trim();
        String correo = txtCorreo.getText().trim();

        // 2. Validación simple
        if(nombre.isEmpty() || apeP.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Debe llenar al menos nombre y apellido");
            return;
        }

        // 3. Conexión e inserción
        Connection con = ConexionBD.getConexion();
        String sql = "INSERT INTO maestros (nombre, apellido_paterno, apellido_materno, especialidad, correo) VALUES (?,?,?,?,?)";
        
        PreparedStatement ps = con.prepareStatement(sql);
        ps.setString(1, nombre);
        ps.setString(2, apeP);
        ps.setString(3, apeM);
        ps.setString(4, esp);
        ps.setString(5, correo);
        
        ps.executeUpdate();
        JOptionPane.showMessageDialog(this, "Maestro registrado correctamente");
        
        con.close();
        // Opcional: limpiar campos tras guardar
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Error al guardar: " + e.getMessage());
    }
        
        
    }//GEN-LAST:event_btnGuardarActionPerformed

