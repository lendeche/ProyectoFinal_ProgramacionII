    public void cargarTablaMaestros() {
    DefaultTableModel modelo = new DefaultTableModel();
    modelo.addColumn("ID");
    modelo.addColumn("Nombre");
    modelo.addColumn("Apellido P.");
    modelo.addColumn("Apellido M.");
    modelo.addColumn("Especialidad");
    modelo.addColumn("Correo");

    String sql = "SELECT * FROM maestros";
    
    try {
        Connection con = Conexion.ConexionBD.getConexion();
        Statement st = con.createStatement();
        ResultSet rs = st.executeQuery(sql);
        
        while (rs.next()) {
            Object[] fila = new Object[6];
            fila[0] = rs.getInt("id_maestro");
            fila[1] = rs.getString("nombre");
            fila[2] = rs.getString("apellido_paterno");
            fila[3] = rs.getString("apellido_materno");
            fila[4] = rs.getString("especialidad");
            fila[5] = rs.getString("correo");
            modelo.addRow(fila);
        }
        
        // Asume que tu tabla en el diseño se llama jTable1
        tblMaestros.setModel(modelo);
        con.close();
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Error al cargar tabla: " + e.getMessage());
    }
}
    
