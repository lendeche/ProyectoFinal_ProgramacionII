    public void listarMaterias() {
    javax.swing.table.DefaultTableModel modelo = new javax.swing.table.DefaultTableModel();
    modelo.addColumn("ID");
    modelo.addColumn("Materia");
    modelo.addColumn("Créditos");
    modelo.addColumn("Maestro"); 
    
    jTable1.setModel(modelo);
    
    // Consulta 100% revisada con tu Workbench: p.nombre y p.apellido_paterno
    String sql = "SELECT m.id_materia, m.nombre_materia, m.creditos, CONCAT(p.nombre, ' ', p.apellido_paterno) AS maestro " +
                 "FROM materias m " +
                 "LEFT JOIN maestros p ON m.id_maestro = p.id_maestro";
                 
    try {
        java.sql.Connection con = Conexion.ConexionBD.getConexion();
        java.sql.PreparedStatement ps = con.prepareStatement(sql);
        java.sql.ResultSet rs = ps.executeQuery();
        
        while (rs.next()) {
            Object[] fila = new Object[4];
            fila[0] = rs.getInt("id_materia");
            fila[1] = rs.getString("nombre_materia");
            fila[2] = rs.getInt("creditos");
            fila[3] = rs.getString("maestro") != null ? rs.getString("maestro") : "Sin asignar";
            modelo.addRow(fila);
        }
    } catch (java.sql.SQLException e) {
        System.out.println("Error al llenar la tabla: " + e.getMessage());
    }
}
    public void cargarComboBoxMaestros() {
    jComboBox1.removeAllItems(); 
    jComboBox1.addItem("Selecciona un maestro...");
    
    // Consulta limpia usando las columnas exactas de tu imagen de Workbench
    String sql = "SELECT nombre, apellido_paterno FROM maestros"; 
    try {
        java.sql.Connection con = Conexion.ConexionBD.getConexion();
        java.sql.PreparedStatement ps = con.prepareStatement(sql);
        java.sql.ResultSet rs = ps.executeQuery();
        
        while (rs.next()) {
            String nombreCompleto = rs.getString("nombre") + " " + rs.getString("apellido_paterno");
            jComboBox1.addItem(nombreCompleto);
        }
    } catch (java.sql.SQLException e) {
        System.out.println("Error al cargar maestros: " + e.getMessage());
    }
}
    public int obtenerIdProfesor(String nombreCompleto) {
    int id = 0;
    String sql = "SELECT id_maestro FROM maestros WHERE CONCAT(nombre, ' ', apellido_paterno) = ?";
    try {
        java.sql.Connection con = Conexion.ConexionBD.getConexion();
        java.sql.PreparedStatement ps = con.prepareStatement(sql);
        ps.setString(1, nombreCompleto);
        java.sql.ResultSet rs = ps.executeQuery();
        if (rs.next()) {
            id = rs.getInt("id_maestro");
        }
    } catch (java.sql.SQLException e) {
        System.out.println("Error al obtener ID del maestro: " + e.getMessage());
    }
    return id;
}

