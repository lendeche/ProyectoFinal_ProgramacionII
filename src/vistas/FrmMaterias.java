    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        if (txtNombreMateria.getText().trim().isEmpty() || txtCreditos.getText().trim().isEmpty()) {
        javax.swing.JOptionPane.showMessageDialog(this, "Todos los campos son obligatorios.", "Error", javax.swing.JOptionPane.WARNING_MESSAGE);
        return;
    }
    
    // Validar que se haya seleccionado un maestro real de la lista
    String maestroSeleccionado = jComboBox1.getSelectedItem().toString();
    if (maestroSeleccionado.equals("Selecciona un maestro...")) {
        javax.swing.JOptionPane.showMessageDialog(this, "Por favor, asigna un maestro a la materia.", "Error", javax.swing.JOptionPane.WARNING_MESSAGE);
        return;
    }

    try {
        // 2. Validación de formato de números
        int creditos = Integer.parseInt(txtCreditos.getText().trim());

        // 3. Consulta SQL incluyendo la columna id_maestro que tiene tu tabla materias
        String sql = "INSERT INTO materias (nombre_materia, creditos, id_maestro) VALUES (?, ?, ?)";
        
        java.sql.Connection con = Conexion.ConexionBD.getConexion();
        java.sql.PreparedStatement ps = con.prepareStatement(sql);
        
        ps.setString(1, txtNombreMateria.getText().trim());
        ps.setInt(2, creditos);
        
        // Buscamos el ID del maestro seleccionado (por ejemplo, "Juan Pérez" -> retorna 1)
        int idMaestro = obtenerIdProfesor(maestroSeleccionado);
        ps.setInt(3, idMaestro);
        
        // 4. EJECUTAR LA INSERCIÓN CORRECTAMENTE 
        int resultado = ps.executeUpdate(); // ⬅️ IMPORTANTE: Cambiado a executeUpdate() estándar
        
        if (resultado > 0) {
            javax.swing.JOptionPane.showMessageDialog(this, "Materia registrada con éxito en la base de datos.", "Éxito", javax.swing.JOptionPane.INFORMATION_MESSAGE);
            
            // Limpiamos los cuadros de texto
            txtNombreMateria.setText("");
            txtCreditos.setText("");
            jComboBox1.setSelectedIndex(0); 
            
            // Recargamos la tabla de la derecha automáticamente
            listarMaterias(); 
        }
        
    } catch (NumberFormatException e) {
        javax.swing.JOptionPane.showMessageDialog(this, "El campo Créditos debe ser un número entero válido.", "Error", javax.swing.JOptionPane.WARNING_MESSAGE);
    } catch (java.sql.SQLException e) {
        javax.swing.JOptionPane.showMessageDialog(this, "Error en la base de datos: " + e.getMessage(), "Error", javax.swing.JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_btnGuardarActionPerformed

    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        // TODO add your handling code here:
        if (txtNombreMateria.getText().trim().isEmpty() || txtCreditos.getText().trim().isEmpty()) {
        javax.swing.JOptionPane.showMessageDialog(this, "Todos los campos son obligatorios para editar.", "Error", javax.swing.JOptionPane.WARNING_MESSAGE);
        return;
    }
    
    try {
        int creditos = Integer.parseInt(txtCreditos.getText().trim());
        String nombre = txtNombreMateria.getText().trim();
        
        // Modifica los créditos basándose en el nombre de la materia
        String sql = "UPDATE materias SET creditos = ? WHERE nombre_materia = ?";
        java.sql.Connection con = Conexion.ConexionBD.getConexion();
        java.sql.PreparedStatement ps = con.prepareStatement(sql);
        ps.setInt(1, creditos);
        ps.setString(2, nombre);
        
        int resultado = ps.executeUpdate();
        if (resultado > 0) {
            javax.swing.JOptionPane.showMessageDialog(this, "Materia actualizada correctamente.", "Éxito", javax.swing.JOptionPane.INFORMATION_MESSAGE);
            listarMaterias(); // Actualiza la tabla
        } else {
            javax.swing.JOptionPane.showMessageDialog(this, "No se encontró la materia para modificar.", "Error", javax.swing.JOptionPane.ERROR_MESSAGE);
        }
    } catch (NumberFormatException e) {
        javax.swing.JOptionPane.showMessageDialog(this, "Créditos debe ser un número válido.", "Error", javax.swing.JOptionPane.ERROR_MESSAGE);
    } catch (java.sql.SQLException e) {
        javax.swing.JOptionPane.showMessageDialog(this, "Error al editar: " + e.getMessage(), "Error", javax.swing.JOptionPane.ERROR_MESSAGE);
      }
    }//GEN-LAST:event_btnEditarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:
        
        String nombre = txtNombreMateria.getText().trim();
    if (nombre.isEmpty()) {
        javax.swing.JOptionPane.showMessageDialog(this, "Escribe el nombre de la materia que deseas eliminar.", "Aviso", javax.swing.JOptionPane.WARNING_MESSAGE);
        return;
    }

    // Cuadro de diálogo de confirmación (Requisito)
    int confirmar = javax.swing.JOptionPane.showConfirmDialog(this, "¿De verdad deseas eliminar la materia " + nombre + "?", "Confirmar", javax.swing.JOptionPane.YES_NO_OPTION);
    
    if (confirmar == javax.swing.JOptionPane.YES_OPTION) {
        try {
            String sql = "DELETE FROM materias WHERE nombre_materia = ?";
            java.sql.Connection con = Conexion.ConexionBD.getConexion();
            java.sql.PreparedStatement ps = con.prepareStatement(sql);
            ps.setString(1, nombre);
            
            int resultado = ps.executeUpdate();
            if (resultado > 0) {
                javax.swing.JOptionPane.showMessageDialog(this, "Materia eliminada con éxito.", "Éxito", javax.swing.JOptionPane.INFORMATION_MESSAGE);
                txtNombreMateria.setText("");
                txtCreditos.setText("");
                listarMaterias(); // Esto actualizará la tabla automáticamente
            } else {
                javax.swing.JOptionPane.showMessageDialog(this, "No se encontró esa materia.", "Error", javax.swing.JOptionPane.ERROR_MESSAGE);
            }
        } catch (java.sql.SQLException e) {
            if (e.getErrorCode() == 1451) { // 1451 es el código de error de MySQL para llaves foráneas
        javax.swing.JOptionPane.showMessageDialog(this, 
            "No se puede eliminar esta materia porque tiene calificaciones o alumnos asignados.", 
            "Acción Denegada", 
            javax.swing.JOptionPane.WARNING_MESSAGE);
    } else {
        javax.swing.JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
    }
}
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

